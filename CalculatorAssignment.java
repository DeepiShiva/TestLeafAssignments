package week1.day2;

public class CalculatorAssignment {

	public int addFunction (int num1, int num2, int num3)
	
	{
	  int sum = num1+num2+num3;
	  return sum;
	}
	
public int subtractFunction (int num1, int num2)
	
	{
	  int difference = num1-num2;
	  return difference;
	}
	
public double multiplyFunction (double num1, double num2)

{
  double product = num1 * num2;
  return product;
}

public float divideFunction (float num1, float num2)

{
  float quotient = num1/num2;
  return quotient;
}
		

}
